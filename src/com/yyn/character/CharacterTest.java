public class CharacterTest {
    public static void main(String[]str){

        /*\t 在文中该处插入一个tab键
        \b	在文中该处插入一个后退键
        \n	在文中该处换行
        \r	在文中该处插入回车
        \f	在文中该处插入换页符
        \'	在文中该处插入单引号
        \"	在文中该处插入双引号
        \\	在文中该处插入反斜杠*/
        System.out.println("\\t-->杨雅娜\t丁作强");
        System.out.println("\\b-->杨雅娜\b丁作强");
        System.out.println("\\n-->杨雅娜\n丁作强");
        System.out.println("\\r-->杨雅娜\r丁作强");
        System.out.println("\\f-->杨雅娜\f丁作强");
        System.out.println("\\'-->杨雅娜\'丁作强");
        System.out.println("\\\"-->杨雅娜\"丁作强");
        System.out.println("\\\\-->杨雅娜\\丁作强");




    }
}
